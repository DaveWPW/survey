package com.dave.entity.vo;

public class ResultQuesInfo {
	private Integer quesNum;
	private String quesType;
	private String quesName;
	private String optionCon;
	
	public Integer getQuesNum() {
		return quesNum;
	}
	public void setQuesNum(Integer quesNum) {
		this.quesNum = quesNum;
	}
	public String getQuesType() {
		return quesType;
	}
	public void setQuesType(String quesType) {
		this.quesType = quesType;
	}
	public String getQuesName() {
		return quesName;
	}
	public void setQuesName(String quesName) {
		this.quesName = quesName;
	}
	public String getOptionCon() {
		return optionCon;
	}
	public void setOptionCon(String optionCon) {
		this.optionCon = optionCon;
	}
}
